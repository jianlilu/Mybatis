package com.lu.pojo;

/*     准备实体类Category，用于映射表category_   */

public class Category {

	private int id;
    private String name;
    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
     
}